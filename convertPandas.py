import pandas as pd

def conver_cvs(a, b):
    """
    a = nama file cvs
    b = nama variabel
    """
    with open(a, "w") as file_csv:
        file_csv.write(b)

def add_time(b, jalur, filename):
    """
    b = data series (panjang data)
    jalur = dataframe
    filename = nama file untuk disimpan
    """
    time = []
    for i in range(1, len(b) + 1):
        time.append(str(i))
    time_list = time
    jalur['waktu'] = time_list
    jalur.to_csv(filename, index=False)


